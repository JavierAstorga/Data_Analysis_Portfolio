#The circle module has functions that perform
#calculations related to circles

import math

#the area function accepts a circle's radius as an argument
# and returns the area of the circle

def area(radius):

    #pi r square

    return math.pi * radius ** 2

#the circunference function accepts a circles radius
# and returns the circles circunferences

def circumference(radius):

    #2 pi r

    return 2 * math.pi *radius

